SELECT
    ITM.PRODUTOID, 
    PRO.CODESTOQUE AS NOME, 
    PRO.DESCRICAO,
    ITM.QTDSALDO,
    ITM.VRUNITARIO,
    ITM.QTDPRODUTO,
	ITM.VRUNITARIO * ITM.QTDPRODUTO AS TOTAL,
	'' AS DISCOUNT_TYPE,
	'' AS RECURRENCE
FROM VDA_PEDIDOITEM ITM
     JOIN EST_PRODUTO PRO ON PRO.PRODUTOID = ITM.PRODUTOID
WHERE ITM.PEDIDOID = :PEDIDOID
ORDER BY PRO.DESCRICAO;
